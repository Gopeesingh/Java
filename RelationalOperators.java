public class RelationalOperators {
    public static void main(String[] args) {
        int p = 10, q = 15;
        System.out.println(p==q); //flase
        System.out.println(p!=q); //true
        System.out.println(p<=q); //true
        System.out.println(p>=q); //flase
        System.out.println(p<q); //true
        System.out.println(p>q); //flase

    }
}
